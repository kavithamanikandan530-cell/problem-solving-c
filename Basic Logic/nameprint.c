int main() {
    char text[100];
    int length = 0;


    printf("Enter a word in CAPITAL letters: ");
    scanf(" %[^\n]s", text);



    while (text[length] != '\0') {
        length++;
    }

    printf("\n--- Result ---\n\n");


    for (int row = 0; row < 6; row++) {


        for (int i = 0; i < length; i++) {
            char ch = text[i];

            int letterIndex = ch - 'A';


            for (int col = 0; col < 6; col++) {
                if (letters[letterIndex][row][col] == 1) {
                    printf("*");
                } else {
                    printf(" ");
                }
            }
            printf("  ");
        }

        printf("\n");
    }

    return 0;
}
